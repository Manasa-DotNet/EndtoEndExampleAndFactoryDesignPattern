    public class GitHubDataService : IGitHubDataService
    {
        public async Task<List<GitHubItem>> GetGitHubRepos()
        {
            try
            {
                HttpClient client = new HttpClient();
                client.DefaultRequestHeaders.Accept.Clear();
                client.DefaultRequestHeaders.Accept.Add(
                    new MediaTypeWithQualityHeaderValue("application/vnd.github.v3+json"));
                client.DefaultRequestHeaders.Add("User-Agent", ".NET Foundation Repository Reporter");

                var response = await client.GetStringAsync("https://api.github.com/repositories?since=364");

                return JsonConvert.DeserializeObject<List<GitHubItem>>(response);
            }
            catch (Exception ex)
            {

                throw;
            }
        }
    }

